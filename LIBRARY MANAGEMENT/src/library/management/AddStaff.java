    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
 String url = "jdbc:mysql://localhost/library";
    String user = "root";
    String pwd = "soorya234";

    // SQL queries
    String insertQuery = "INSERT INTO STAFF (STAFF_ID, NAME, CONTACT) VALUES (?, ?, ?)";
    String updateQuery = "UPDATE STAFF SET CONTACT = ? WHERE STAFF_ID = ? AND NAME = ?";

    // Get input values
    String id = T1.getText();
    String name = T2.getText();
    String contact = T3.getText(); // Assuming contact is a string, not an int

    try {
        // Establish connection
        Connection conn = DriverManager.getConnection(url, user, pwd);

        // Check if the staff member already exists and update the contact if they do
        PreparedStatement checkStmt = conn.prepareStatement(updateQuery);
        checkStmt.setString(1, contact);
        checkStmt.setString(2, id);
        checkStmt.setString(3, name);

        int rows = checkStmt.executeUpdate();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Contact updated successfully");
        } else {
            // If the staff member does not exist, insert a new record
            PreparedStatement insertStmt = conn.prepareStatement(insertQuery);
            insertStmt.setString(1, id);
            insertStmt.setString(2, name);
            insertStmt.setString(3, contact);

            insertStmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "One record added successfully");
        }

        // Clear the input fields
        T1.setText(null);
        T2.setText(null);
        T3.setText(null);

        // Close statements
        checkStmt.close();
        conn.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

