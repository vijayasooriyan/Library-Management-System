    private void passwordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passwordActionPerformed
        // TODO add your handling code here:
    String url = "jdbc:mysql://localhost:3306/library?useSSL=false";
    String mysqluser = "root";
    String mysqlpwd = "soorya234";
    String pswrd = new String(password.getPassword());
    String username = user.getText();
    String query = "SELECT PASSWORD FROM Admin WHERE USER_ID = ?";

    Connection conn = null;
    PreparedStatement pstmt = null;
    ResultSet rs = null;

    try {
        conn = DriverManager.getConnection(url, mysqluser, mysqlpwd);
        pstmt = conn.prepareStatement(query);
        pstmt.setString(1, username);
        rs = pstmt.executeQuery();

        if (rs.next()) {
            String realpswrd = rs.getString("PASSWORD"); // Ensure the column name matches the database
            if (realpswrd.equals(pswrd)) {
                dashboard dsh = new dashboard();
                dsh.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Username or password entered is wrong");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Wrong username");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
        e.printStackTrace(); // Log the stack trace for debugging
    } finally {
        try {
            if (rs != null) rs.close();
            if (pstmt != null) pstmt.close();
            if (conn != null) conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_passwordActionPerformed

