    private void A1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_A1ActionPerformed
        // TODO add your handling code here:
            String url = "jdbc:mysql://localhost/library";
    String user = "root";
    String pwd = "soorya234";
    
    // SQL queries
    String insertQuery = "INSERT INTO books (BOOK_ID, CATEGORY, NAME, AUTHOR, COPIES) VALUES (?, ?, ?, ?, ?)";
    String updateQuery = "UPDATE books SET COPIES = COPIES + ? WHERE NAME = ? AND CATEGORY = ? AND AUTHOR = ?";

    // Get input values
    String id = t1.getText();
    String category = t2.getText();
    String name = t3.getText();
    String author = t4.getText();
    int copies = Integer.parseInt(t5.getText());
    
    try {
        // Establish connection
        Connection conn = DriverManager.getConnection(url, user, pwd);
        
        // Check if the book already exists and update the copies if it does
        PreparedStatement checkStmt = conn.prepareStatement(updateQuery);
        checkStmt.setInt(1, copies);
        checkStmt.setString(2, name);
        checkStmt.setString(3, category);
        checkStmt.setString(4, author);
        
        int rows = checkStmt.executeUpdate();
        
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Copies updated successfully");
        } else {
            // If the book does not exist, insert a new record
            PreparedStatement insertStmt = conn.prepareStatement(insertQuery);
            insertStmt.setString(1, id);
            insertStmt.setString(2, category);
            insertStmt.setString(3, name);
            insertStmt.setString(4, author);
            insertStmt.setInt(5, copies);
            
            insertStmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "One record added successfully");
        }

        // Clear the input fields
        t1.setText(null);
        t2.setText(null);
        t3.setText(null);
        t4.setText(null);
        t5.setText(null);

        // Close statements
        checkStmt.close();
        conn.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_A1ActionPerformed

